def main() -> None:
    smoke = os.environ.get("NYCOPT_SMOKE") == "1"
    n_profiles = 24 if smoke else int(os.environ.get("NYCOPT_NPROFILES", "1000"))
    slugs = {
        "mean_only": f"kn_inputlhs_meanonly_{REALIZATION_YEARS}yr_n{n_profiles}",
        "mean_var": f"kn_inputlhs_meanvar_{REALIZATION_YEARS}yr_n{n_profiles}",
    }

    hist = load_historical_flows(gage=False, period="full")
    reference_daily = hist.loc[:, list(DEFAULT_NYC_INFLOW_NODES)].sum(axis=1)
    reference_monthly = daily_to_monthly(reference_daily, agg="mean")
    reference_daily = reference_daily.to_numpy(dtype=float)

    rungs, results = {}, {}
    for name, slug in slugs.items():
        print(f"[diag] loading rung '{name}' ({slug})...")
        rung = load_rung(slug, reference_monthly, reference_daily)
        rungs[name] = rung
        print(f"[diag] analyzing rung '{name}' (M={rung['n_master']})...")
        results[name] = {
            "slug": slug,
            "coverage": analyze_rung(rung),
            "reachability": reachability(rung),
        }

    head = compute_headline(results, rungs)
    vd = verdict(head, results, rungs)

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    payload = {
        "fixed_axes": list(FIXED_AXES),
        "n_sweep": list(N_SWEEP),
        "subset_seeds": list(SUBSET_SEEDS),
        "rungs": {n: {"slug": results[n]["slug"], "n_master": rungs[n]["n_master"]} for n in rungs},
        "results": results,
        "headline": head,
        "verdict": vd,
    }
    (OUT_DIR / "results.json").write_text(json.dumps(payload, indent=2, default=float))
    print(f"[diag] wrote {OUT_DIR / 'results.json'}")

    print("[diag] building figures...")
    from src.plotting import style
    style.apply_style()
    make_figures(rungs, results, style, OUT_DIR / "figures")

    write_memo(OUT_DIR / "FINDINGS.md", rungs, results, head, vd)
    print(f"[diag] wrote {OUT_DIR / 'FINDINGS.md'}")
    print(f"[diag] VERDICT: premise {vd['verdict']} "
          f"(discrepancy_ratio={vd['discrepancy_ratio']:.2f}, "
          f"hazfill_pctile={vd['hazard_filling_percentile']:.2f}, "
          f"input_pctile={vd['input_stratified_percentile']:.2f})")


if __name__ == "__main__":
    main()
