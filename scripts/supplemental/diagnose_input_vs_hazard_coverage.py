    L.append("Discrepancy percentile = fraction of random designs at matched N that this design beats "
             "on L2-star (lower = more uniform than chance).\n")
    L.append("| rung | N | L2 ratio (input/hazfill) | hazfill pctile | input pctile | hazfill ESS | input ESS |")
    L.append("|---|---|---|---|---|---|---|")
    for r, h in head.items():
        L.append(f"| {r} | {h['headline_n']} | {h['hazard_l2_ratio_input_over_hazfill']:.2f} | "
                 f"{h['hazard_pctile_hazard_filling']:.2f} | {h['hazard_pctile_input_stratified']:.2f} | "
                 f"{h['hazard_ess_hazard_filling']:.1f} | {h['hazard_ess_input_stratified']:.1f} |")
    L.append("")

    L.append("## Hazard-axis reachability from input space (max |Spearman|)\n")
    L.append("Low values mark forcing-limited axes the input space barely moves "
             "(cannot fairly be called 'uncovered by input sampling').\n")
    L.append("| axis | " + " | ".join(rungs) + " |")
    L.append("|---|" + "|".join(["---"] * len(rungs)) + "|")
    for axis in FIXED_AXES:
        row = [axis] + [f"{results[r]['reachability'][axis]['max_abs_spearman']:.2f}" for r in rungs]
        L.append("| " + " | ".join(row) + " |")
    L.append("")

    L.append("## Coverage vs N (hazard space, mean over seeds)\n")
    for r in rungs:
        L.append(f"### {r}\n")
        by_n = results[r]["coverage"]["spaces"]["hazard"]["by_n"]
        L.append("| N | design | L2-star | NN-CV | MST | ESS | disc. pctile |")
        L.append("|---|---|---|---|---|---|---|")
        for n in sorted(by_n):
            for design in DESIGNS:
                d = by_n[n][design]
                L.append(f"| {n} | {design} | {d['l2_star_mean']:.4f} | {d['nn_cv_mean']:.2f} | "
                         f"{d['mst_mean_mean']:.3f} | {d['ess_mean']:.1f} | "
                         f"{d['discrepancy_percentile_mean']:.2f} |")
        L.append("")

    L.append("## Caveats\n")
    L.append("- 5-yr realizations truncate the ~4–5-yr DRB drought of record, limiting "
             "multi-year drought-duration coverage; duration-type axes may read forcing-limited.")
    L.append("- No persistence or daily-extreme/flood-intensity forcing axis this round (deferred); "
             "the input space spans CMIP6 mean + an independent CV (variance) axis.")
    L.append("- Coverage is evaluated in empirical-CDF-normalized space; the input-stratified "
             "selector uses min-max normalization, so evaluation is independent of selection scaling.\n")
    Path(path).write_text("\n".join(L), encoding="utf-8")


