"""Diagnose input-space vs hazard-space coverage on the input-spanning masters.

Decision-gating diagnostic for the hazard-filling contribution. From each richness-ladder master
(generated by ``generate_input_spanning_master.py``) it forms three size-N subsets with the SAME
LHS+nearest-neighbor selector applied in different spaces:

  - ``input_stratified`` -- space-filling in INPUT (forcing-parameter) space (theta),
  - ``hazard_filling``    -- space-filling in HAZARD space (the 4 screened axes),
  - ``random``            -- random-without-replacement baseline,

then quantifies, in BOTH spaces, coverage (L2-star discrepancy, NN-CV, MST mean) and a kernel
effective-sample-size (ESS) redundancy measure. Because all three subsample the *same* theta-spanning
master, any hazard-coverage difference is attributable to the selection criterion, not the generator.

The richness ladder (``mean_only`` vs ``mean_var``) tests whether the input!=hazard gap is a generic
mechanism or an artifact of an impoverished (mean-only) input space: the premise is credible only if
the gap persists under the enriched input space.

Outputs to ``outputs/diagnostics/input_vs_hazard_coverage/``: ``results.json``, ``figures/*.png``,
``FINDINGS.md``.

Run from the repo root::

    PYTHONPATH=$(pwd -W) venv/Scripts/python.exe scripts/supplemental/diagnose_input_vs_hazard_coverage.py
"""

