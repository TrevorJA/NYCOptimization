def _subset_coverage(Xnorm, sel, baseline_samples):
    """Coverage metrics for one subset in a CDF-normalized space."""
    d = Xnorm.shape[1]
    n = len(sel)
    cm = coverage_metrics(Xnorm[sel], np.zeros(d), np.ones(d))
    l2 = cm["L2_star_discrepancy"]
    ess = hazard_effective_sample_size(Xnorm[sel])  # default discriminating bandwidth
    pct = float(np.mean(baseline_samples <= l2)) if baseline_samples is not None else float("nan")
    return {
        "l2_star": l2,
        "nn_cv": cm.get("nn_cv", float("nan")),
        "nn_min": cm.get("nn_min", float("nan")),
        "mst_mean": mst_mean_edge(Xnorm[sel]),
        "ess": ess["ess"],
        "ess_fraction": ess["ess_fraction"],
        "discrepancy_percentile": pct,
    }


def _agg(records):
    """Mean/std over seed replicates for each metric."""
    keys = records[0].keys()
    out = {}
    for k in keys:
        vals = np.array([r[k] for r in records], dtype=float)
        out[f"{k}_mean"] = float(np.nanmean(vals))
        out[f"{k}_std"] = float(np.nanstd(vals))
    return out


def analyze_rung(rung):
    """Coverage + ESS in hazard and input space across designs, N, seeds."""
    H4, Theta = rung["H4"], rung["Theta"]
    M = rung["n_master"]
    spaces = {
        "hazard": (H4, empirical_cdf_normalize(H4)),
        "input": (Theta, empirical_cdf_normalize(Theta)),
    }
    n_sweep = [n for n in N_SWEEP if n < M] or [max(4, M // 4), max(8, M // 2)]

    results = {"n_master": M, "n_sweep": n_sweep, "spaces": {}}
    for space_name, (_, Xnorm) in spaces.items():
        d = Xnorm.shape[1]
        results["spaces"][space_name] = {"dimensions": d, "by_n": {}}
        for n in n_sweep:
            base = expected_random_discrepancy(n, d, n_boot=N_BOOT, seed=0, pool_X=Xnorm)
            base_samples = np.asarray(base["samples"], dtype=float)
            per_design = {}
            for design in DESIGNS:
                recs = [
                    _subset_coverage(
                        Xnorm, _select_indices(design, H4, Theta, n, s), base_samples
                    )
                    for s in SUBSET_SEEDS
                ]
                per_design[design] = _agg(recs)
            per_design["_random_baseline_l2"] = {
                "mean": float(base["mean"]), "std": float(base["std"])
            }
            results["spaces"][space_name]["by_n"][n] = per_design
    return results


def reachability(rung):
    """How far the input space moves each hazard axis (max |Spearman| over input columns)."""
    H4, Theta = rung["H4"], rung["Theta"]
    out = {}
    for j, axis in enumerate(FIXED_AXES):
        rs = [abs(spearmanr(Theta[:, k], H4[:, j]).statistic) for k in range(Theta.shape[1])]
        out[axis] = {"max_abs_spearman": float(np.nanmax(rs))}
    return out


