def compute_headline(results, rungs):
    """Discrepancy ratio / percentiles / ESS (hazard-filling vs input-stratified) at the headline N.

    The primary signal is the hazard-space L2-star discrepancy and its percentile against the random
    baseline at matched N (lower discrepancy / lower percentile = more uniform coverage). ESS is a
    corroborating-but-weak redundancy summary.
    """
    head = {}
    for rung_name, rung in rungs.items():
        n = _headline_n(rung["n_master"])
        haz = results[rung_name]["coverage"]["spaces"]["hazard"]["by_n"][n]
        inp = results[rung_name]["coverage"]["spaces"]["input"]["by_n"][n]
        l2_hf = haz["hazard_filling"]["l2_star_mean"]
        l2_is = haz["input_stratified"]["l2_star_mean"]
        head[rung_name] = {
            "headline_n": n,
            "hazard_l2_ratio_input_over_hazfill": l2_is / l2_hf if l2_hf else float("nan"),
            "hazard_pctile_hazard_filling": haz["hazard_filling"]["discrepancy_percentile_mean"],
            "hazard_pctile_input_stratified": haz["input_stratified"]["discrepancy_percentile_mean"],
            "hazard_ess_hazard_filling": haz["hazard_filling"]["ess_mean"],
            "hazard_ess_input_stratified": haz["input_stratified"]["ess_mean"],
            "hazard_ess_gap": haz["hazard_filling"]["ess_mean"] - haz["input_stratified"]["ess_mean"],
            "hazard_nn_cv_input_stratified": haz["input_stratified"]["nn_cv_mean"],
            "hazard_nn_cv_hazard_filling": haz["hazard_filling"]["nn_cv_mean"],
            "input_l2_input_stratified": inp["input_stratified"]["l2_star_mean"],
            "input_l2_hazard_filling": inp["hazard_filling"]["l2_star_mean"],
        }
    return head


def verdict(head, results, rungs):
    """Nuanced verdict, judged at the enriched (fair-test) rung.

    Anchored on hazard-space L2 discrepancy: does hazard-filling cover hazard space markedly better
    than input-space LHS, and does that advantage survive enriching the input space (the fair test)?
    """
    key = "mean_var" if "mean_var" in head else next(iter(head))
    h = head[key]
    ratio = h["hazard_l2_ratio_input_over_hazfill"]
    pct_hf = h["hazard_pctile_hazard_filling"]
    pct_is = h["hazard_pctile_input_stratified"]
    mirror_ok = h["input_l2_input_stratified"] < h["input_l2_hazard_filling"]
    hf_beats_random = pct_hf < 0.15           # hazard-filling reliably better than random
    input_redundant = pct_is > 0.35           # input-LHS no better than random in hazard space

    # How much of the impoverished-rung redundancy was an input-poverty artifact.
    artifact_fraction = float("nan")
    if "mean_only" in head and "mean_var" in head:
        r_imp = head["mean_only"]["hazard_l2_ratio_input_over_hazfill"]
        r_enr = head["mean_var"]["hazard_l2_ratio_input_over_hazfill"]
        if r_imp > 1:
            artifact_fraction = (r_imp - r_enr) / (r_imp - 1.0)

    if hf_beats_random and ratio >= 1.5 and input_redundant and mirror_ok:
        v = "HOLDS (strong)"
    elif hf_beats_random and ratio >= 1.2 and mirror_ok:
        v = "HOLDS (moderate)"
    elif hf_beats_random and ratio >= 1.05:
        v = "WEAK"
    else:
        v = "FAILS"
    return {
        "verdict": v, "judged_rung": key, "discrepancy_ratio": ratio,
        "hazard_filling_percentile": pct_hf, "input_stratified_percentile": pct_is,
        "hf_beats_random": bool(hf_beats_random), "input_redundant": bool(input_redundant),
        "mirror_ok": bool(mirror_ok), "artifact_fraction": artifact_fraction,
    }


