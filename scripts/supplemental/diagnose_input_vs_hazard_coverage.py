def _select_indices(design, matrix_hazard, Theta, n, seed):
    """Return n selected row indices for a design (selection space differs by design)."""
    if design == "input_stratified":
        return input_stratified_subsample(Theta, n, seed=seed)
    if design == "hazard_filling":
        return hazard_filling_subsample(matrix_hazard, n, seed=seed)
    if design == "random":
        return random_subsample(matrix_hazard, n, seed=seed)
    raise ValueError(design)


def load_rung(slug, reference_monthly, reference_daily):
    """Load a master, compute its hazard image (4 fixed axes) and input coordinates theta."""
    pool_dir = staged_ensemble_dir(slug)
    pool_hdf5 = pool_dir / "catchment_inflow_mgd.hdf5"
    if not pool_hdf5.exists():
        raise FileNotFoundError(f"master not staged: {pool_hdf5}")

    scenario_monthly, realization_ids = load_ensemble_monthly_aggregate(
        pool_hdf5, DEFAULT_NYC_INFLOW_NODES, agg="mean"
    )
    scenario_daily, _ = load_ensemble_daily_aggregate(pool_hdf5, DEFAULT_NYC_INFLOW_NODES)

    H_all, axes_all = compute_candidate_hazard_image(
        scenario_monthly, scenario_daily, reference_monthly, reference_daily
    )
    col = {a: i for i, a in enumerate(axes_all)}
    missing = [a for a in FIXED_AXES if a not in col]
    if missing:
        raise KeyError(f"fixed axes missing from hazard image: {missing}")
    H4 = H_all[:, [col[a] for a in FIXED_AXES]]

    npz = np.load(pool_dir / "forcing_profiles.npz", allow_pickle=True)
    a = npz["mean_factor_a"].astype(float)
    if "cv_factor_v" in npz:
        Theta = np.hstack([a, npz["cv_factor_v"].astype(float)])
    else:
        Theta = a
    return {
        "slug": slug,
        "H4": H4,
        "Theta": Theta,
        "a": a,
        "scenario_monthly": scenario_monthly,
        "n_master": H4.shape[0],
    }


