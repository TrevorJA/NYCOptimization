def _load_mean_envelope():
    from scengen import forcing_space as fs
    csv = (Path(config.PROJECT_DIR).parent / "CMIP6_multimodel_streamflow" / "stats"
           / "diff_relative_to_dataset_baseline"
           / "nyc_inflow_monthly_mean_frac_by_dataset_ssp_and_period.csv")
    return fs.load_cmip6_envelope(csv)


def _fig_forcing_profiles(rung, mean_env, style, fig_dir, plt):
    a = rung["a"]
    months = np.arange(1, 13)
    fig, ax = plt.subplots(figsize=(7, 4))
    ax.plot(months, mean_env.values, color="0.6", lw=0.6, alpha=0.5)
    ax.plot(months, a[:: max(1, len(a) // 200)].T, color="#1f77b4", lw=0.4, alpha=0.3)
    ax.fill_between(months, mean_env.values.min(1), mean_env.values.max(1),
                    color="0.8", alpha=0.4, label="CMIP6 envelope")
    ax.plot(months, mean_env.values.mean(1), color="k", lw=2, label="CMIP6 mean")
    ax.set(xlabel="Month (water-year order)", ylabel="Mean change factor $a_j$",
           title="F1. Sampled mean change-factor profiles vs CMIP6 anchors")
    ax.legend(fontsize=8)
    style.save_figure(fig, fig_dir / "F1_mean_change_profiles")
    plt.close(fig)

    if rung["Theta"].shape[1] > 12:
        v = rung["Theta"][:, 12:]
        fig, ax = plt.subplots(figsize=(7, 4))
        ax.plot(months, v[:: max(1, len(v) // 200)].T, color="#d62728", lw=0.4, alpha=0.3)
        ax.axhline(1.0, color="k", lw=1, ls="--", label="CV-preserving ($v_j=1$)")
        ax.set(xlabel="Month (water-year order)", ylabel="CV change factor $v_j$",
               title="F2. Sampled CV (variance) change-factor profiles")
        ax.legend(fontsize=8)
        style.save_figure(fig, fig_dir / "F2_cv_change_profiles")
        plt.close(fig)

    # F3 PC scatter: anchors vs sampled (mean space, log).
    anchors = np.log(mean_env.values.T)
    samp = np.log(rung["a"])
    mu = anchors.mean(0)
    _, _, Vt = np.linalg.svd(anchors - mu, full_matrices=False)
    sa = (anchors - mu) @ Vt[:2].T
    ss = (samp - mu) @ Vt[:2].T
    fig, ax = plt.subplots(figsize=(5.5, 5))
    ax.scatter(ss[:, 0], ss[:, 1], s=6, alpha=0.3, color="#1f77b4", label="sampled $\\theta$")
    ax.scatter(sa[:, 0], sa[:, 1], s=40, color="k", marker="x", label="CMIP6 anchors")
    ax.set(xlabel="PC1", ylabel="PC2", title="F3. Forcing draws vs CMIP6 anchors (PC space)")
    ax.legend(fontsize=8)
    style.save_figure(fig, fig_dir / "F3_forcing_pc_scatter")
    plt.close(fig)


def _fig_hydrographs(rung, style, fig_dir, plt):
    sm = rung["scenario_monthly"]
    nm = sm.shape[1] - sm.shape[1] % 12
    cycle = sm[:, :nm].reshape(sm.shape[0], -1, 12).mean(1)  # mean annual cycle per realization
    order = np.argsort(sm.mean(1))
    picks = {"dry (10th pct)": order[len(order) // 10],
             "median": order[len(order) // 2],
             "wet (90th pct)": order[9 * len(order) // 10]}
    months = np.arange(1, 13)
    fig, ax = plt.subplots(figsize=(7, 4))
    for label, idx in picks.items():
        ax.plot(months, cycle[idx], lw=2, label=label)
    ax.plot(months, cycle.mean(0), color="k", lw=1.5, ls="--", label="master mean")
    ax.set(xlabel="Month (water-year order)", ylabel="Mean NYC inflow (MGD)",
           title="F4. Example realized annual cycles (climate-adjusted)")
    ax.legend(fontsize=8)
    style.save_figure(fig, fig_dir / "F4_example_hydrographs")
    plt.close(fig)


def _fig_space_scatter(rung, space, style, fig_dir, plt, rung_name):
    X = rung["H4"] if space == "hazard" else rung["Theta"]
    Xn = empirical_cdf_normalize(X)
    P, _ = _pca_2d(Xn)
    n = _headline_n(rung["n_master"])
    fig, ax = plt.subplots(figsize=(6, 5.5))
    ax.scatter(P[:, 0], P[:, 1], s=5, color="0.8", alpha=0.5, label="master")
    for design in DESIGNS:
        sel = _select_indices(design, rung["H4"], rung["Theta"], n, 0)
        ax.scatter(P[sel, 0], P[sel, 1], s=22, alpha=0.85,
                   color=DESIGN_COLORS[design], label=design, edgecolor="white", linewidth=0.3)
    ax.set(xlabel="PC1", ylabel="PC2",
           title=f"{'F5' if space=='hazard' else 'F6'}. {space.title()}-space coverage "
                 f"(N={n}, {rung_name})")
    ax.legend(fontsize=8, markerscale=1.2)
    tag = "F5_hazard_scatter" if space == "hazard" else "F6_input_scatter"
    style.save_figure(fig, fig_dir / f"{tag}_{rung_name}")
    plt.close(fig)


def _fig_metric_vs_n(res, space, metric, ylabel, style, fig_dir, plt, rung_name):
    by_n = res["spaces"][space]["by_n"]
    ns = sorted(by_n)
    fig, ax = plt.subplots(figsize=(6, 4.5))
    for design in DESIGNS:
        means = [by_n[n][design][f"{metric}_mean"] for n in ns]
        stds = [by_n[n][design][f"{metric}_std"] for n in ns]
        ax.errorbar(ns, means, yerr=stds, marker="o", capsize=3,
                    color=DESIGN_COLORS[design], label=design)
    if metric == "l2_star":
        rb = [by_n[n]["_random_baseline_l2"]["mean"] for n in ns]
        ax.plot(ns, rb, color="k", ls=":", lw=1, label="random baseline (bootstrap)")
    ax.set(xlabel="Subset size N", ylabel=ylabel,
           title=f"{'F7' if metric=='l2_star' else 'F8'}. {ylabel} ({rung_name})")
    ax.set_xticks(ns)
    ax.legend(fontsize=8)
    tag = "F7_l2_vs_n" if metric == "l2_star" else "F8_ess_vs_n"
    style.save_figure(fig, fig_dir / f"{tag}_{space}_{rung_name}")
    plt.close(fig)


def _fig_input_to_hazard_map(rung, reach, style, fig_dir, plt, rung_name):
    P, _ = _pca_2d(empirical_cdf_normalize(rung["Theta"]))
    fig, axes = plt.subplots(2, 2, figsize=(10, 8.5), constrained_layout=True)
    for ax, axis in zip(axes.ravel(), FIXED_AXES):
        vals = rung["H4"][:, FIXED_AXES.index(axis)]
        sc = ax.scatter(P[:, 0], P[:, 1], c=vals, s=8, cmap="viridis")
        fig.colorbar(sc, ax=ax, shrink=0.8)
        r = reach[axis]["max_abs_spearman"]
        ax.set(xlabel="input PC1", ylabel="input PC2",
               title=f"{axis}\nmax|Spearman(theta, axis)|={r:.2f}")
    fig.suptitle(f"F9. Input space -> hazard distribution ({rung_name})", fontsize=12)
    style.save_figure(fig, fig_dir / f"F9_input_to_hazard_map_{rung_name}")
    plt.close(fig)


def _headline_n(M):
    valid = [n for n in N_SWEEP if n < M]
    if not valid:
        return max(4, M // 2)
    return 64 if 64 in valid else max(valid)


